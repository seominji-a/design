    private void Search_BTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Search_BTActionPerformed

        String name = Id_Text.getText();

        int numa = 0;
        int numb = 0;
        int numc = 0;

        String temps = "";
        String temps1 = "";
        String temps2 = "";
        String temps3 = "";

        String a = "";
        String b = "";
        String c = "";

        String[] resulta;
        String[] resulta1;
        String[] resulta2;
        String[] resulta3;

        DefaultTableModel search;
        search = (DefaultTableModel) jTable1.getModel();

        try {
            BufferedReader txtread = new BufferedReader(new FileReader(new File("C:\\DB\\Book.txt")));

            BufferedReader txtread1 = new BufferedReader(new FileReader(new File("C:\\DB\\seat.txt")));

            BufferedReader txtread2 = new BufferedReader(new FileReader(new File("C:\\DB\\seat2.txt")));

            BufferedReader txtread3 = new BufferedReader(new FileReader(new File("C:\\DB\\seat3.txt")));

            if (name.length() == 0) {
                JOptionPane.showMessageDialog(null, "ID를 확인하세요");
            } else {
                while ((temps1 = txtread1.readLine()) != null) {
                    resulta1 = temps1.split(" ");
                    if (name.equals(resulta1[1])) {
                        numa = 1;
                        a = resulta1[0];
                    }
                }

                while ((temps2 = txtread2.readLine()) != null) {
                    resulta2 = temps2.split(" ");
                    if (name.equals(resulta2[1])) {
                        numb = 1;
                        b = resulta2[0];
                    }
                }

                while ((temps3 = txtread3.readLine()) != null) {
                    resulta3 = temps3.split(" ");
                    if (name.equals(resulta3[1])) {
                        numc = 1;
                        c = resulta3[0];
                    }
                }

                if (numa == 1) {
                    while ((temps = txtread.readLine()) != null) {
                        resulta = temps.split(" ");
                        if (name.equals(resulta[0])) {
                            search.insertRow(search.getRowCount(), new Object[]{
                                resulta[0],
                                resulta[1],
                                resulta[2],
                                resulta[3],
                                resulta[4] + "-" + resulta[5] + "-" + resulta[6],
                                resulta[4] + "-" + resulta[7] + "-" + resulta[8],
                                a
                            });
                        }
                    }
                }

                if (numb == 1) {
                    while ((temps = txtread.readLine()) != null) {
                        resulta = temps.split(" ");
                        if (name.equals(resulta[0])) {
                            search.insertRow(search.getRowCount(), new Object[]{
                                resulta[0],
                                resulta[1],
                                resulta[2],
                                resulta[3],
                                resulta[4] + "-" + resulta[5] + "-" + resulta[6],
                                resulta[4] + "-" + resulta[7] + "-" + resulta[8],
                                b
                            });
                        }
                    }
                }

                if (numc == 1) {
                    while ((temps = txtread.readLine()) != null) {
                        resulta = temps.split(" ");
                        if (name.equals(resulta[0])) {
                            if (numc == 1) {
                                search.insertRow(search.getRowCount(), new Object[]{
                                    resulta[0],
                                    resulta[1],
                                    resulta[2],
                                    resulta[3],
                                    resulta[4] + "-" + resulta[5] + "-" + resulta[6],
                                    resulta[4] + "-" + resulta[7] + "-" + resulta[8],
                                    c
                                });
                            }
                        }
                    }
                }

                txtread.close();
                txtread1.close();
                txtread2.close();
                txtread3.close();
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ChangeLine.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ChangeLine.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Search_BTActionPerformed

