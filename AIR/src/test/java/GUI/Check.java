    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int row = jTable1.getSelectedRow();
        int col = jTable1.getSelectedColumn();
        String a = (String) jTable1.getValueAt(row, 0);

        String[] cancel;
        String[] rm1;
        String[] rm2;
        String[] rm3;

        String addcancel = "";
        String dseat1 = "";
        String dseat2 = "";
        String dseat3 = "";

        String line;
        String dline1;
        String dline2;
        String dline3;

        FileInputStream add;
        FileInputStream delete1;
        FileInputStream delete2;
        FileInputStream delete3;

        try {
            add = new FileInputStream(booking);
            InputStreamReader isr = new InputStreamReader(add);
            BufferedReader cadd = new BufferedReader(isr);

            delete1 = new FileInputStream(scheck1);
            InputStreamReader isr1 = new InputStreamReader(delete1);
            BufferedReader cadd1 = new BufferedReader(isr1);

            delete2 = new FileInputStream(scheck2);
            InputStreamReader isr2 = new InputStreamReader(delete2);
            BufferedReader cadd2 = new BufferedReader(isr2);

            delete3 = new FileInputStream(scheck3);
            InputStreamReader isr3 = new InputStreamReader(delete3);
            BufferedReader cadd3 = new BufferedReader(isr3);

            while ((line = cadd.readLine()) != null) {
                cancel = line.split(" ");

                if (!a.equals(cancel[0])) {
                    addcancel += (line + "\r\n");
                    s = 1;
                }
            }
            if (s == 1) {
                JOptionPane.showMessageDialog(null, "취소되었습니다.");
            }

            while ((dline1 = cadd1.readLine()) != null) {
                rm1 = dline1.split(" ");

                if (!a.equals(rm1[1])) {
                    dseat1 += (dline1 + "\r\n");
                    s = 1;
                }
            }
            while ((dline2 = cadd2.readLine()) != null) {
                rm2 = dline2.split(" ");

                if (!a.equals(rm2[1])) {
                    dseat2 += (dline2 + "\r\n");
                    s = 1;
                }
            }
            while ((dline3 = cadd3.readLine()) != null) {
                rm3 = dline3.split(" ");

                if (!a.equals(rm3[1])) {
                    dseat3 += (dline3 + "\r\n");
                    s = 1;

                }
            }
            FileWriter addfw = new FileWriter(booking);
            addfw.write(addcancel);
            addfw.close();

            FileWriter deletefw1 = new FileWriter(scheck1);
            deletefw1.write(dseat1);
            deletefw1.close();

            FileWriter deletefw2 = new FileWriter(scheck2);
            deletefw2.write(dseat2);
            deletefw2.close();

            FileWriter deletefw3 = new FileWriter(scheck3);
            deletefw3.write(dseat3);
            deletefw3.close();

            cadd.close();
            cadd1.close();
            cadd2.close();
            cadd3.close();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(admain.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(admain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

